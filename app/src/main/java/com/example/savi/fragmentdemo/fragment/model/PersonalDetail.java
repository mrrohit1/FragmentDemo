package com.example.savi.fragmentdemo.fragment.model;

public class PersonalDetail {
    private String name;
    private String age;
    private String gender;
    private String contactNo;
    private String companyName;
    private String salary;
    private String experience;
    private String universsityName;
    private String yearOfPssing;
    private String percentage;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getContactNo() {
        return contactNo;
    }

    public void setContactNo(String contactNo) {
        this.contactNo = contactNo;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public String getExperience() {
        return experience;
    }

    public void setExperience(String experience) {
        this.experience = experience;
    }

    public String getUniverssityName() {
        return universsityName;
    }

    public void setUniverssityName(String universsityName) {
        this.universsityName = universsityName;
    }

    public String getYearOfPssing() {
        return yearOfPssing;
    }

    public void setYearOfPssing(String yearOfPssing) {
        this.yearOfPssing = yearOfPssing;
    }

    public String getPercentage() {
        return percentage;
    }

    public void setPercentage(String percentage) {
        this.percentage = percentage;
    }
}
